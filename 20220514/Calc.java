public class Calc {

	public static void main(String[] args) {
		int number1 = 100, number2 = 2;
		System.out.println("+ : " + (number1+number2));
		System.out.println("- : " + (number1-number2));
		System.out.println("x :" + (number1*number2));
		System.out.println("/ :" + (number1/number2));
	}

}
